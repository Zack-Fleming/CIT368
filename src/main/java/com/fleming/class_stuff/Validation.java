package com.fleming.class_stuff;

public class Validation
{
    final static String PATTERN_NAME = "^[a-zA-Z ]+$",
            PATTERN_AGE = "^[0-9]{1,3}$",
            PATTERN_ZIPCODE = "^[0-9]{5}$",
            PATTERN_PHONE = "^([0-9]{3}-)?[0-9]{3}-[0-9]{4}$",
            PATTERN_SOCIAL = "^[0-9]{3}-[0-9]{2}-[0-9]{4}$",
            PATTERN_CARD = "^([0-9]{4}-){3}[0-9]{4}$",
            PATTERN_IP = "^([0-9]{1,3}\\.){3}[0-9]{1,3}$",
            PATTERN_EMAIL = "^.+@.+\\..+$",
            PATTERN_WEB = "^(https?:\\/\\/)?(w{3})?\\.?.+\\..+$";


}
